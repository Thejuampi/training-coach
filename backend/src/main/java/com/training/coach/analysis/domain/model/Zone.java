package com.training.coach.analysis.domain.model;

/**
 * Training intensity zones following the Seiler 3-zone model.
 */
public enum Zone {
    /** Zone 1: Low intensity, below LT1, FATMAX region */
    Z1,

    /** Zone 2: Moderate intensity, between LT1 and LT2 */
    Z2,

    /** Zone 3: High intensity, at or above LT2 */
    Z3
}
